    {
        $this->validate($request, [
            'name' => 'required',
            'weapon_description' => 'sometimes|max:255'
        ]);

        $player = new Player();
        $player->name = $request->name;
        $player->weapon_description = $request->weapon_description;


        $weapon->save();

        flash('New Weapon Add Sussess')->success();

